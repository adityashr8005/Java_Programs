package DualLoop;

//3 to 7 tables.
public class Loop0 {
    public static void main(String[] args) {

        for (int num1=3 ; num1<=7 ; num1++){
            for(int i=1 ; i<=10 ; i++){
                System.out.print(num1*i+" ");
            }
            System.out.println();
        }
    }
}
